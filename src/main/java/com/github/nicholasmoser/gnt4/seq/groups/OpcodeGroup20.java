  public static Opcode parse(ByteStream bs, byte opcodeByte) throws IOException {
    return switch (opcodeByte) {
      case 0x00 -> op_2000(bs);
      case 0x01 -> op_2001(bs);
      case 0x02 -> op_2002(bs);
      case 0x03 -> op_2003(bs);
      case 0x04 -> op_2004(bs);
      case 0x05 -> op_2005(bs);
      case 0x07 -> op_2007(bs);
      case 0x08 -> op_2008(bs);
      case 0x09 -> op_2009(bs);
      case 0x0B -> op_200B(bs);
      case 0x0C -> op_200C(bs);
      case 0x10 -> op_2010(bs);
      case 0x11 -> op_2011(bs);
      case 0x12 -> op_2012(bs);
      case 0x13 -> op_2013(bs);
      case 0x14 -> op_2014(bs);
      case 0x15 -> op_2015(bs);
      case 0x16 -> op_2016(bs);
      case 0x17 -> op_2017(bs);
      case 0x18 -> op_2018(bs);
      case 0x19 -> op_2019(bs);
      case 0x1A -> op_201A(bs);
      case 0x1B -> op_201B(bs);
      case 0x1C -> op_201C(bs);
      case 0x1D -> op_201D(bs);
      case 0x1E -> op_201E(bs);
      case 0x1F -> op_201F(bs);
      case 0x20 -> op_2020(bs);
      case 0x21 -> op_2021(bs);
      case 0x22 -> UnknownOpcode.of(0x20, 0x22, 0x4, bs);
      case 0x23 -> op_2023(bs);
      case 0x24 -> op_2024(bs);
      case 0x25 -> UnknownOpcode.of(0x20, 0x25, 0x4, bs);
      case 0x26 -> UnknownOpcode.of(0x20, 0x26, 0x4, bs);
      case 0x27 -> UnknownOpcode.of(0x20, 0x27, 0x4, bs);
      case 0x28 -> op_2028(bs);
      case 0x29 -> op_2029(bs);
      case 0x2A -> op_202A(bs);
      case 0x2C -> op_202C(bs);
      case 0x2D -> op_202D(bs);
      case 0x2F -> op_202F(bs);
      case 0x32 -> op_2032(bs);
      case 0x33 -> op_2033(bs);
      case 0x36 -> op_2036(bs);
      case 0x37 -> op_2037(bs);
      case 0x38 -> op_2038(bs);
      case 0x39 -> op_2039(bs);
      case 0x3B -> op_203B(bs);
      case 0x3C -> op_203C(bs);
      case 0x3D -> op_203D(bs);
      case 0x3E -> op_203E(bs);
      case 0x3F -> op_203F(bs);
      case 0x40 -> op_2040(bs);
      case 0x41 -> op_2041(bs);
      case 0x42 -> op_2042(bs);
      default -> throw new IOException(String.format("Unimplemented: %02X", opcodeByte));
    };
  }

  private static Opcode op_2000(ByteStream bs) throws IOException {
    int offset = bs.offset();
    EffectiveAddresses ea = EffectiveAddresses.get(bs);
    String info = String.format(" %s", ea.getDescription());
    return new UnknownOpcode(offset, ea.getBytes(), info);
  }

